package src.basics_baatein;

public class bodmas_rule {
    public static void main(String[] args) {
        int x = 10;
        System.out.println(x);

        x += 5;
        System.out.println(x);

        x *= 3;
        System.out.println(x);

        x -= 5;
        System.out.println(x);

        x = 14;
        System.out.println(x++);
        System.out.println(x);
    }
}
