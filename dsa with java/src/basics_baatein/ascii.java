package src.basics_baatein;

public class ascii {
    public static void main(String[] args) {

        // TYPECASTING : AKE DATA TYPE SE DUSRA DATA TYPE CONVERSION

        char ch = 'A';
        int x = ch;
        System.out.println(x);

        char piyush = '$';
        int y = piyush;
        System.out.println(y);

        char chh = 'b';
        System.out.println(chh+0);

        char chr = 'c';
        System.out.println(chr + chr);

        int r = 66;
        char rv = (char)r;
        System.out.println(rv);
    }
}
